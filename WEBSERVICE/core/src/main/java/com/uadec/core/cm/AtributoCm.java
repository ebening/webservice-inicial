package com.uadec.core.cm;

public class AtributoCm implements Cloneable {

	private String name = "";
	private Object valor;
	private Class<?> clazz;
	private String description = "";
	private short tipo;
	private int longuitud;
	private boolean requerido;
	private boolean representativo;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	public Object getValor() {
		return valor;
	}

	public void setValor(Object valor) {
		this.valor = valor;
	}

	public Class<?> getClazz() {
		return clazz;
	}

	public void setClazz(Class<?> clazz) {
		this.clazz = clazz;
	}

	public int getLonguitud() {
		return longuitud;
	}

	public void setLonguitud(int longuitud) {
		this.longuitud = longuitud;
	}

	public boolean isRequerido() {
		return requerido;
	}

	public void setRequerido(boolean requerido) {
		this.requerido = requerido;
	}

	public short getTipo() {
		return tipo;
	}

	public void setTipo(short tipo) {
		this.tipo = tipo;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public boolean isRepresentativo() {
		return representativo;
	}

	public void setRepresentativo(boolean representativo) {
		this.representativo = representativo;
	}

	@Override
	public String toString() {
		return "\n     Nombre=" + name + "\n    Desc=" + description;
	}
}